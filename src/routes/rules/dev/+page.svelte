  <h2>Releases</h2>
  <p>
    Cubix considers a "complete" game to be any game that can be beaten from start to finish without paying any extra fees to unlock further content. Upon public release, any title submitted to Cubix must be complete. All games not in a complete state will not be eligble to be monetized via the platform, will not be publicly available via the eShop without an access code and will be denoted as "Incomplete" until it is considered complete.
  </p>
  <h3 class="ind">
    DLC and Updates
  </h3>
  <p class="margin ind">
    DLC (Downloadable Content) should be for purchasable products that provide qualitatively new content. Cubix reviews all DLC submissions before they are posted on the SNext eShop to assure that they follow all of our Developer Guidelines and our Terms Of Service. Cubix maintains the right to deny any DLC that we deem is not of quality and will provide feedback on how to improve the DLC for passing standards.
  </p>
  <p class="margin ind">
    Updates are considered <b>free</b> changes to a game. They will change the version number of the game and may be minor changes such as bug patches and other free fixes or adjustments. Updates can included new content, however paid "updates" are treated as DLC and are subject to those rules.
  </p>
  <h2>Quality Standards</h2>
  <p>
    Cubix allows anyone to submit a game via our Developer Portal. All submitted games are sent to be reviewed by a Quality Assurance staff members and feedback on your submission will be available with a few days after your submission. When submitting a title, Cubix completes a three part examination that includes checking performance, code legitimacy and game content.
  </p>
